bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    /*
     * Keychron keeps USB enumerated while its hardware switch selects
     * Bluetooth. Suppress controls on the dedicated layer in that state so
     * no F13-F24 shortcut leaks to the Bluetooth host.
     */
    if (get_transport() != TRANSPORT_USB) {
        if (layer_switch_get_layer(record->event.key) == OPENCONTROL && keycode != OC_TOGG) {
            return false;
        }
        return true;
    }

    return process_record_opencontrol(keycode, record);
}

